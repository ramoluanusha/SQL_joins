 HAVING TOTAL_REVENUE>50000;
 
 -- Q25. Find cities having more than 10 customers.
 SELECT C.CITY,COUNT(O.CUSTOMER_ID) AS COUNT_CUSTOMERS
 FROM CUSTOMERS AS C INNER JOIN ORDERS AS O
 ON C.CUSTOMER_ID=O.CUSTOMER_ID
 GROUP BY C.CITY
 HAVING COUNT_CUSTOMERS>10;
 
-- Q26. Find the top 5 highest-selling products.
SELECT P.PRODUCT_NAME, COUNT(OI.PRODUCT_ID) AS TOP5_PRODUCTS
FROM PRODUCTS AS P INNER JOIN ORDER_ITEMS AS OI
ON P.PRODUCT_ID=OI.PRODUCT_ID
GROUP BY P.PRODUCT_NAME
ORDER BY TOP5_PRODUCTS DESC
LIMIT 5 ;

-- Q27. Find the top 3 most profitable products.
SELECT PRODUCT_NAME,SUM(PRICE) AS TOP3_PROFITABLE_PRODUCTS
FROM PRODUCTS
GROUP BY PRODUCT_NAME
ORDER BY  TOP3_PROFITABLE_PRODUCTS DESC
LIMIT 3;

-- Q28. Find customers whose spending is above average customer spending.
SELECT C.CUSTOMER_NAME, SUM((OI.QUANTITY * OI.UNIT_PRICE)) AS TOTAL_SPENDING
FROM ORDER_ITEMS AS OI INNER JOIN ORDERS AS O
ON OI.ORDER_ID=O.ORDER_ID
INNER JOIN CUSTOMERS AS C
ON C.CUSTOMER_ID=O.CUSTOMER_ID
GROUP BY C.CUSTOMER_NAME
HAVING TOTAL_SPENDING >
(
SELECT AVG(customer_total)
FROM
(
 SELECT SUM(OI.QUANTITY * OI.UNIT_PRICE) AS customer_total
	FROM ORDER_ITEMS AS OI INNER JOIN ORDERS AS O
	ON OI.ORDER_ID = O.ORDER_ID
	GROUP BY O.CUSTOMER_ID
    ) AS avg_table
);

