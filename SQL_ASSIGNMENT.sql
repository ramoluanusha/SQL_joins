-- PART 2: Revenue & Sales Analysis
-- Business Requirement: The finance department wants summarized revenue and sales insights.

-- Q11. Find total revenue generated by each category.

SELECT CATEGORY,SUM(PRICE) AS TOTAL_REVENUE FROM PRODUCTS
GROUP BY CATEGORY ;

-- Q12. Find total quantity sold for each product.

SELECT P.PRODUCT_NAME,SUM(O.QUANTITY) AS TOTAL_QUANTITY FROM order_items AS O
INNER JOIN products AS P
ON P.product_id=O.ORDER_ID
GROUP BY product_name;

-- Q13. Find total orders under each order status

SELECT STATUS,COUNT(ORDER_ID) TOTAL_ORDERS FROM ORDERS
GROUP BY STATUS;

-- Q14. Find total revenue generated through each payment method.

SELECT O.PAYMENT_METHOD,SUM(P.PRICE) AS TOTAL_REVENUE FROM ORDERS AS O
JOIN ORDER_ITEMS AS IO
ON O.ORDER_ID=IO.ORDER_ID
JOIN PRODUCTS AS P
ON P.PRODUCT_ID=IO.PRODUCT_ID
GROUP BY O.PAYMENT_METHOD
ORDER BY TOTAL_REVENUE DESC
LIMIT 5;

-- Q15. Find average product price category-wise.

SELECT CATEGORY,AVG(PRICE) AS AVERAGE_PRICE FROM PRODUCTS 
GROUP BY CATEGORY;

-- Q16. Find category-wise quantity sold.

SELECT P.CATEGORY ,COUNT(O.QUANTITY) AS QUANTITY_SOLD FROM products AS P
INNER JOIN order_items AS O
ON P.PRODUCT_ID=O.PRODUCT_ID
GROUP BY P.CATEGORY;

-- Q17. Find monthly order count.
SELECT month(o.order_date) as monthly_sales,COUNT(distinct io.ORDER_ID) as MONTHLY_ORDER_COUNT FROM ORDERS as o
inner join order_items as io
on o.order_id=io.order_id
group by  month(o.order_date);

-- Q18. Find monthly revenue trend.

SELECT MONTH(O.ORDER_DATE) AS MONTHLY_SALES,SUM(OI.QUANTITY * OI.UNIT_PRICE) AS TOTAL_REVENUE
FROM ORDERS AS O JOIN ORDER_ITEMS AS OI
ON O.ORDER_ID =OI.ORDER_ID
GROUP BY MONTH(O.ORDER_DATE);

-- Q19. Find the most commonly used payment method.
SELECT PAYMENT_METHOD,COUNT(PAYMENT_METHOD) AS MOST_COMMON_PAYMENT FROM ORDERS
GROUP BY PAYMENT_METHOD
ORDER BY MOST_COMMON_PAYMENT DESC ;

-- Q20. Find the highest revenue-generating category.
SELECT CATEGORY,MAX(PRICE) AS HIGHEST_REVENUE FROM PRODUCTS
GROUP BY CATEGORY
ORDER BY HIGHEST_REVENUE DESC
LIMIT 1;

-- PART 3: High-Value Business Insights
-- Business Requirement: Management wants to identify high-performing customers, products, and regions.

-- Q21. Find customers who placed more than 5 orders.

SELECT C.CUSTOMER_NAME,COUNT(OI.ORDER_ID) AS TOTAL_ORDERS
FROM CUSTOMERS AS C INNER JOIN ORDERS AS O
ON C.CUSTOMER_ID=O.CUSTOMER_ID
JOIN  ORDER_ITEMS AS OI
ON O.ORDER_ID=OI.ORDER_ID
GROUP BY C.CUSTOMER_NAME ;

-- Q22. Find top 5 customers based on total spending.

SELECT C.CUSTOMER_NAME, SUM(OI.QUANTITY * OI.UNIT_PRICE) AS TOTAL_SPEND_AMOUNT FROM
 ORDER_ITEMS AS OI INNER JOIN ORDERS AS O
 ON OI.ORDER_ID=O.ORDER_ID
 INNER JOIN CUSTOMERS AS C
 ON C.CUSTOMER_ID=O.CUSTOMER_ID
 GROUP BY CUSTOMER_NAME
 ORDER BY TOTAL_SPEND_AMOUNT DESC
 LIMIT 5;
 
 -- Q23. Find products sold more than 30 times.
 SELECT P.PRODUCT_NAME,SUM(OI.QUANTITY) AS TOTAL_SOLD FROM PRODUCTS AS P
 INNER JOIN ORDER_ITEMS AS OI
 ON P.PRODUCT_ID=OI.PRODUCT_ID
 GROUP BY P.PRODUCT_NAME
 HAVING TOTAL_SOLD>30;

 -- Q24. Find categories generating revenue greater than 50000.
 SELECT CATEGORY,SUM(PRICE) AS TOTAL_REVENUE FROM PRODUCTS
