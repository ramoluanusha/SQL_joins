-- Business Requirement: Management wants to identify high-performing customers, products, and regions.

-- Q21. Find customers who placed more than 5 orders.

SELECT C.CUSTOMER_NAME,COUNT(OI.ORDER_ID) AS TOTAL_ORDERS
FROM CUSTOMERS AS C INNER JOIN ORDERS AS O
ON C.CUSTOMER_ID=O.CUSTOMER_ID
JOIN  ORDER_ITEMS AS OI
ON O.ORDER_ID=OI.ORDER_ID
GROUP BY C.CUSTOMER_NAME ;

-- Q22. Find top 5 customers based on total spending.

SELECT C.CUSTOMER_NAME, SUM(OI.QUANTITY * OI.UNIT_PRICE) AS TOTAL_SPEND_AMOUNT FROM
 ORDER_ITEMS AS OI INNER JOIN ORDERS AS O
 ON OI.ORDER_ID=O.ORDER_ID
 INNER JOIN CUSTOMERS AS C
 ON C.CUSTOMER_ID=O.CUSTOMER_ID
 GROUP BY CUSTOMER_NAME
 ORDER BY TOTAL_SPEND_AMOUNT DESC
 LIMIT 5;
 
 -- Q23. Find products sold more than 30 times.
