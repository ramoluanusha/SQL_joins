LIMIT 3;

-- Q28. Find customers whose spending is above average customer spending.
SELECT C.CUSTOMER_NAME, SUM((OI.QUANTITY * OI.UNIT_PRICE)) AS TOTAL_SPENDING
FROM ORDER_ITEMS AS OI INNER JOIN ORDERS AS O
ON OI.ORDER_ID=O.ORDER_ID
INNER JOIN CUSTOMERS AS C
ON C.CUSTOMER_ID=O.CUSTOMER_ID
GROUP BY C.CUSTOMER_NAME
HAVING TOTAL_SPENDING >
(
SELECT AVG(customer_total)
FROM
(
 SELECT SUM(OI.QUANTITY * OI.UNIT_PRICE) AS customer_total
	FROM ORDER_ITEMS AS OI INNER JOIN ORDERS AS O
	ON OI.ORDER_ID = O.ORDER_ID
	GROUP BY O.CUSTOMER_ID
    ) AS avg_table
);

