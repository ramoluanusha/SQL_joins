-- Q38. Find products whose stock is lower than total quantity sold.
select p.product_name,p.stock,sum(oi.quantity) as total_quantity_sold from PRODUCTS as p inner join ORDER_ITEMS as oi 
on p.product_id=oi.product_id
group by p.product_name,p.stock
having p.stock<sum(oi.quantity);

-- Q39. Find category-wise revenue contribution percentage.
SELECT p.category,SUM(oi.quantity * p.price) AS category_revenue,
    ROUND(
        (SUM(oi.quantity * p.price) * 100.0) /
        (SELECT SUM(oi2.quantity * p2.price)
         FROM order_items oi2
         JOIN products p2
             ON oi2.product_id = p2.product_id),
    2) AS revenue_percentage
FROM products AS p
JOIN order_items AS oi
    ON p.product_id = oi.product_id
GROUP BY p.category;

-- Q40. Find repeat customers who ordered in multiple months.
select c.customer_name,count(date_format(order_date,"%Y-%M")) as active_customer
from customers as c
join orders as o
on c.customer_id = o.customer_id
group by c.customer_name
having active_customer >1
order by active_customer desc;


